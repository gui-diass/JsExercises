// Faça um Programa que leia um número e exiba o dia correspondente da semana. (1-Domingo, 2- Segunda, etc.), se digitar outro valor deve aparecer valor inválido.

const dia = 1

 if (dia == 1){
    console.log('Domimgo')
} 
else if (dia == 2){
    console.log('Segunda')
} 
else if (dia == 3){
    console.log('Terça')
} 
else if (dia === 4){
    console.log('Quarta')
} 
else if (dia === 5){
    console.log('Quinta')
} 
else if (dia === 6){
    console.log('Sexta')
} 
else if (dia === 7){
    console.log('Sábado')
} 
else{
    console.log('Valor inválido')
} 
